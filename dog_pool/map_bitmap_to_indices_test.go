func TestMapBitmapToIndicesSpecs(t *testing.T) {
	if testing.Short() {
		t.Skip("skipping test in benchmark mode.")
		return
	}
	r := gospec.NewRunner()
	r.AddSpec(MapBitmapToIndicesSpecs)
	gospec.MainGoTest(r, t)
}

func MapBitmapToIndicesSpecs(c gospec.Context) {

	// NOTE: Bitmaps are reverse! (Left -> Right)
	c.Specify("[MapBitmapToIndices] Decodes bitmap", func() {
		values := MapBitmapToIndices([]byte{})
		c.Expect(values, gospec.Satisfies, nil != values)
		c.Expect(len(values), gospec.Equals, 0)
	})

	// NOTE: Bitmaps are reverse! (Left -> Right)
	c.Specify("[MapBitmapToIndices] Decodes bitmap", func() {
		values := MapBitmapToIndices([]byte{0xF0})
		c.Expect(values, gospec.Satisfies, nil != values)
		c.Expect(len(values), gospec.Equals, 4)
		c.Expect(values[0], gospec.Equals, int64(0))
		c.Expect(values[1], gospec.Equals, int64(1))
		c.Expect(values[2], gospec.Equals, int64(2))
		c.Expect(values[3], gospec.Equals, int64(3))
	})

	// NOTE: Bitmaps are reverse! (Left -> Right)
	c.Specify("[MapBitmapToIndices] Decodes bitmap", func() {
		values := MapBitmapToIndices([]byte{0x0F})
		c.Expect(values, gospec.Satisfies, nil != values)
		c.Expect(len(values), gospec.Equals, 4)
		c.Expect(values[0], gospec.Equals, int64(4))
		c.Expect(values[1], gospec.Equals, int64(5))
		c.Expect(values[2], gospec.Equals, int64(6))
		c.Expect(values[3], gospec.Equals, int64(7))
	})

	// NOTE: Bitmaps are reverse! (Left -> Right)
	c.Specify("[MapBitmapToIndices] Decodes bitmap", func() {
		values := MapBitmapToIndices([]byte{0xBC})
		c.Expect(values, gospec.Satisfies, nil != values)
		c.Expect(len(values), gospec.Equals, 5)
		c.Expect(values[0], gospec.Equals, int64(0))
		c.Expect(values[1], gospec.Equals, int64(2))
		c.Expect(values[2], gospec.Equals, int64(3))
		c.Expect(values[3], gospec.Equals, int64(4))
		c.Expect(values[4], gospec.Equals, int64(5))
	})

	// NOTE: Bitmaps are reverse! (Left -> Right)
	c.Specify("[MapBitmapToIndices] Decodes bitmap", func() {
		values := MapBitmapToIndices([]byte{0xFF})
		c.Expect(values, gospec.Satisfies, nil != values)
		c.Expect(len(values), gospec.Equals, 8)
		c.Expect(values[0], gospec.Equals, int64(0))
		c.Expect(values[1], gospec.Equals, int64(1))
		c.Expect(values[2], gospec.Equals, int64(2))
		c.Expect(values[3], gospec.Equals, int64(3))
		c.Expect(values[4], gospec.Equals, int64(4))
		c.Expect(values[5], gospec.Equals, int64(5))
		c.Expect(values[6], gospec.Equals, int64(6))
		c.Expect(values[7], gospec.Equals, int64(7))
	})

	// NOTE: Bitmaps are reverse! (Left -> Right)
	c.Specify("[MapBitmapToIndices] Decodes bitmap", func() {
		values := MapBitmapToIndices([]byte{0x14, 0x88})
		c.Expect(values, gospec.Satisfies, nil != values)
		c.Expect(len(values), gospec.Equals, 4)
		c.Expect(values[0], gospec.Equals, int64(3))
		c.Expect(values[1], gospec.Equals, int64(5))
		c.Expect(values[2], gospec.Equals, int64(8))
		c.Expect(values[3], gospec.Equals, int64(12))
	})

}

